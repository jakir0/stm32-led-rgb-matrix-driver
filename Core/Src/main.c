void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){

	if(!waiting_for_frame){
		switch(command_buff[0]){
			case 1+48:
				if(is_connected){
					break;
				}
				HAL_UART_Transmit_DMA(&huart2, command_buff, 1);
				is_connected = 1;
				HAL_UART_Receive_DMA(&huart2, command_buff, 1);
				break;

			case 2+48:
				is_connected = 0;
				HAL_UART_Receive_DMA(&huart2, command_buff, 1);
				break;
			case 3+48:
				if(!is_connected){
					break;
				}
				HAL_UART_Transmit_DMA(&huart2, command_buff, 1);
				waiting_for_frame = 1;
				HAL_UART_Receive_DMA(&huart2, recive_buff, 320);
				break;

			default:
				HAL_UART_Receive_DMA(&huart2, command_buff, 1);
				break;
		}
	}else{
		waiting_for_frame = 0;
		HAL_UART_Transmit_DMA(&huart2, recive_buff, 320);
		recieve_buffer_full = 1;
		HAL_UART_Receive_DMA(&huart2, command_buff, 1);
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){

	if(htim == &htim3){
		number_of_tim3_interrupt++;

		if(times_latched<15){
			latch_command=DATA_LATCH;
		}
		else{
			latch_command=GLOBAL_LATCH;
		}

		switch(latch_command){

			case DATA_LATCH:
				times_latched++;
				HAL_GPIO_WritePin(GPIOC, GPIO_PIN_8, GPIO_PIN_SET);
				number_of_data_latches++;
				latch_command=0;
				if(times_latched<14){
					TIM3->ARR=79;
				}
				else{
					TIM3->ARR=77;
				}
				TIM10->ARR=29;
				HAL_TIM_Base_Start_IT(&htim10);
				break;

			case GLOBAL_LATCH:
				TIM3->ARR=83;
				times_latched=0;
				HAL_GPIO_WritePin(GPIOC, GPIO_PIN_8, GPIO_PIN_SET);
				number_of_global_latches++;
				latch_command=0;
				TIM10->ARR=149;
				HAL_TIM_Base_Start_IT(&htim10);
				break;

			default:
				break;
		}
	}

	else if(htim == &htim10){

		HAL_TIM_Base_Stop_IT(&htim10);
		number_of_tim10_interrupt++;
		HAL_GPIO_WritePin(GPIOC, GPIO_PIN_8, GPIO_PIN_RESET);
		HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);


	}
}

